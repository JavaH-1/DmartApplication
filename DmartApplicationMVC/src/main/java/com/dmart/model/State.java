package com.dmart.model;

public class State {
	
    // Getters and Setters
    private int id;
    private String stateName;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getStateName() {
		return stateName;
	}
	public void setStateName(String stateName) {
		this.stateName = stateName;
	}
  
}
